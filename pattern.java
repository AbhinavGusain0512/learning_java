

class Main {
    public static void main(String[] args) {
        for (int i = 0; i < 3; i++) {
            for (int j = 1; j < i + 2; j++) {
                System.out.print(j);

            }
            System.out.println();
        }
        for (int k = 3; k >= 0; k--) {
            for (int l = 1; l < k + 2; l++) {
                System.out.print(l);

            }
            System.out.println();

        }
    }
}
